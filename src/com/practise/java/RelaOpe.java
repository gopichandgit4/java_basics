package com.practise.java;
import java.io.*;

public class RelaOpe {
    public static void main (String[] args){
        int a = 11;
        int b = 10;
        System.out.println("a > b = " + (a>b));
        System.out.println("a < b = " + (a<b));
        System.out.println("a > b = " + (a>b));
        System.out.println("a >= b = " + (a>=b));
        System.out.println("a <= b = " + (a>=b));
        System.out.println("a == b = " + (a==b));
        System.out.println("a != b = " + (a!=b));
    }
}
